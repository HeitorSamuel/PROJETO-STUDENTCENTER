function toggleDropdownmat() {
  var dropdown = document.getElementById("dropdown_matematica");
  if (dropdown.style.display === "none" || dropdown.style.display === "") {
    dropdown.style.display = "block";
  } else {
    dropdown.style.display = "none";
  }
}

